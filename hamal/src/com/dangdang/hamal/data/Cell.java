package com.dangdang.hamal.data;

/**
 * 数据单元格
 * @author zhangkunjs
 *
 */
public class Cell {

	private Object v;
	private boolean upd;	
	private String n;
	public Object getV() {
		return v;
	}
	public void setV(Object v) {
		this.v = v;
	}
	public boolean isUpd() {
		return upd;
	}
	public void setUpd(boolean upd) {
		this.upd = upd;
	}
	public String getN() {
		return n;
	}
	public void setN(String n) {
		this.n = n;
	}


	
}
